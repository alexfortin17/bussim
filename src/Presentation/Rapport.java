    public void remplirTableauStat()
    {
        jTextAreaStat.removeAll();
        Simulation sim = application.getSimParNom(nomSimulation);
        jTextAreaStat.append("Statistiques de la simulation " + sim.getNomSim() + "\n\n" + "\r");
        jTextAreaStat.append("Liste des itin√©raires: " + "\n" + "\r");
        jTextAreaStat.append("Nom \t");
        jTextAreaStat.append("Minimum \t");
        jTextAreaStat.append("Moyenne \t");
        jTextAreaStat.append("Maximum \t \n");        
        ArrayList<Statistique> stat = application.getStatsIti(sim);
        //Iterator<Statistique> ite = stat.iterator();
        for(int i = 0; i < stat.size(); i++)
        {
            Statistique uneStat = stat.get(i);
            jTextAreaStat.append(uneStat.getNomIti() + "\t" + uneStat.getMin() + "\t" + uneStat.getMoyenne() + "\t" + uneStat.getMax() + "\n" + "\r");
        }
    }
    
    
